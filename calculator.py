def add(a,b):
    return a+b

def subtract(a,b):
    return a-b

def multiply(a,b):
    return a*b

def divide(a,b):
    return a/b

def get_remainder(a,b):
    return a//b

def median(a,b):
    return (a+b)/2

def get_abs(num):
    if num>=0:
        return num
    else:
        return -num

def get_Percent(a,b):
    return (a/b) * 100

